function checkLevels()
{
	for(var i = 0; i < passedLevel.length; i++)
	{
		if(passedLevel[i] == 0)
		{	
			document.getElementById("lvl" + (i + 1)).classList.remove("locked");
			document.getElementById("lvl" + (i + 1)).classList.add("standart");
		}
		else if(passedLevel[i] == 1)
		{	
			document.getElementById("lvl" + (i + 1)).classList.remove("standart");
			document.getElementById("lvl" + (i + 1)).classList.add("passed");
		}
		else if(passedLevel[i] == 3)
		{
			document.getElementById("lvl" + (i + 1)).classList.add("locked");
		}
	}
}

